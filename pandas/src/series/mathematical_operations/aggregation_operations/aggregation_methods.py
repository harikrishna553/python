import pandas as pd

series1 = pd.Series([4, 2, 8, 6])

print('series1')
print(series1)

print('\nMean of all the elements : ', series1.mean())
print('Median of all the elements : ', series1.median())
print('Maximum of all the elements : ', series1.max())
print('Minimum of all the elements : ', series1.min())
print('Standard deviation of all the elements : ', series1.std())
print('Sum of all the elements : ', series1.sum())
print('Variance of all the elements : ', series1.var())


