# Iterate the elements from 0 to 9
print('Iterate the elements from 0 to 9')
for item in range(10):
    print(item)

# Iterate over the elements from 5 to 10
print('\nIterate the elements from 5 to 10')
for item in range(5, 10):
    print(item)

# Iterate over the elements from 5 to 20 and specify step size as 3
print('\nIterate the elements from 5 to 20 and specify step size as 3')
for item in range(5, 20, 3):
    print(item)